// Bundle every packed tileset (a *.png with a sibling *.json object manifest) into ONE JS file
// that create-level.html auto-loads on open — no file picking, works even on a double-clicked
// file:// page (a <script src> sibling loads fine; only fetch()/XHR is blocked under file://,
// which is why we inline the PNGs as data: URIs rather than fetching them).
//
// Re-run this whenever you pack a new sheet (build-bangkok-sheet.mjs etc.) and the new objects
// appear in the editor palette automatically.
//
//   node world/client/tools/tileset/build-editor-assets.mjs
import { readFileSync, readdirSync, writeFileSync, existsSync, copyFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve, basename } from 'node:path';
import { fileURLToPath } from 'node:url';

const HERE = dirname(fileURLToPath(import.meta.url));
const TILESET_DIR = resolve(HERE, '../../public/assets/tileset');
const OUT = resolve(HERE, 'editor-assets.js');

// The editor uses the themed library ME_Editor (built from the theme singles). ME_Bangkok is the
// separate Bangkok-street/video sheet and is intentionally NOT in the editor. Add more sheets here
// as they're packed.
const INCLUDE = ['ME_Editor_32x32'];

const bundle = [];
for (const f of readdirSync(TILESET_DIR)) {
  if (!f.endsWith('.json')) continue;
  if (!INCLUDE.includes(f.replace(/\.json$/, ''))) continue;
  const png = f.replace(/\.json$/, '.png');
  const pngPath = resolve(TILESET_DIR, png);
  if (!existsSync(pngPath)) continue;
  const man = JSON.parse(readFileSync(resolve(TILESET_DIR, f), 'utf8'));
  if (!man.objects || !Object.keys(man.objects).length) continue;   // no placeable objects → skip
  const b64 = readFileSync(pngPath).toString('base64');
  bundle.push({
    name: basename(png, '.png'),
    image: png,                       // the path the exported map should reference (../tileset/<png>)
    cols: man.cols || 32,
    rows: man.rows || 0,
    tilecount: man.tilecount || 0,
    dataURI: `data:image/png;base64,${b64}`,
    objects: man.objects,
  });
  console.log(`+ ${basename(png, '.png').padEnd(30)} ${Object.keys(man.objects).length} objects, ${(b64.length / 1024 | 0)}KB`);
}

if (!bundle.length) { console.error('No packed tileset+manifest pairs found in', TILESET_DIR); process.exit(1); }

// ---------------------------------------------------------------------------
// GROUND PRESETS — a DECLARATIVE, JSON-serialisable spec the editor interprets to fill the whole
// Ground layer. (A build() function can't survive JSON, so instead of code we ship data.) The editor
// (create-level.html) turns each preset into a Ground array; this file only DESCRIBES them.
//
// SCHEMA (agreed with the editor):
//   window.GROUND_PRESETS = { "<Name>": { needs:[objKey,...], bands:[ band, ... ] }, ... }
//     needs : terrain object KEYS this preset requires. A preset is offered ONLY if every key exists
//             in a loaded sheet (so "Grass" stays hidden until grass tiles are packed).
//     bands : horizontal strips painted TOP-to-BOTTOM, in order. Each band:
//       fill   : (required) the object KEY to fill this strip with.
//       toFrac : (optional, 0..1) the strip ENDS at row floor(H*toFrac) (exclusive); the next band
//                begins there. Omit on the LAST band to run it to the bottom of the map.
//       scatter: (optional) array of extra object KEYS randomly mixed into the fill, to break up the
//                visible repeat of a single tile (grass tufts, water ripples). Interpreter picks
//                per-cell at random among [fill, ...scatter].
// Only the three below are emitted; each key they name (grass/grass2-4, sand, water/water2-3, pave)
// is packed in ME_Bangkok_32x32 with layer 'Ground', solid 0. Grass and Beach were RENDERED onto a
// 40x30 map and looked (preset-grass.png / preset-beach.png): grass tiles seamlessly, beach reads as
// sand-meets-water. ("Bangkok street" is a built-in editor preset and is intentionally not repeated.)
const GROUND_PRESETS = {
  Grass: { needs: ['grass'], bands: [{ fill: 'grass', scatter: ['grass2', 'grass3', 'grass4'] }] },
  Beach: { needs: ['sand', 'water'], bands: [{ toFrac: 0.6, fill: 'sand' }, { fill: 'water', scatter: ['water2', 'water3'] }] },
  City: { needs: ['pave'], bands: [{ fill: 'pave' }] },
};

writeFileSync(OUT, `// AUTO-GENERATED by build-editor-assets.mjs — do not edit. Re-run to refresh.\n` +
  `window.EDITOR_ASSETS = ${JSON.stringify(bundle)};\n` +
  `window.GROUND_PRESETS = ${JSON.stringify(GROUND_PRESETS)};\n`);
const kb = (readFileSync(OUT).length / 1024) | 0;
console.log(`\nwrote ${OUT} — ${bundle.length} sheet(s), ${kb}KB total, ${Object.keys(GROUND_PRESETS).length} ground presets (${Object.keys(GROUND_PRESETS).join(', ')}).`);

// Publish the editor page + bundle to the MAIN app's public/ so it's served on the Groupifier site
// (same-origin with /api/maps → cookie auth + autosave work). Served at /level-creator/.
const SITE = resolve(HERE, '../../../../public/level-creator');
mkdirSync(SITE, { recursive: true });
copyFileSync(resolve(HERE, 'create-level.html'), resolve(SITE, 'index.html'));
copyFileSync(OUT, resolve(SITE, 'editor-assets.js'));
console.log(`published editor → public/level-creator/  (served at /level-creator/)`);
